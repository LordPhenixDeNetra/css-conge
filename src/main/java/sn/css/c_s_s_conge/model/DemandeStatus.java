/**
 * Enumération représentant les différents statuts d'une demande.
 */
public enum DemandeStatus {

    /**
     * La demande est en cours de traitement.
     */
    EN_COURS_DE_TRAITEMENT,

    /**
     * La demande est en attente de compléments d'information.
     */
    EN_ATTENTE_DE_COMPLEMENTS,

    /**
     * La demande est terminée.
     */
    TERMINER
}
